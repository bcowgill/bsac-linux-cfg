#!/bin/bash
# set vars for Google Chrome Browser backups

source setup-main.sh

GOOGSUBDIR="Google\Chrome\User Data"
ROOTGOOGDIR="$LOCALAPPDATA\\$GOOGSUBDIR"
USERGOOGDIR="$USERLOCALAPPDATA\\$GOOGSUBDIR"
ROOTGOOGLOCALSTGDIR="$ROOTGOOGDIR\\Default\\Local Storage"
USERGOOGLOCALSTGDIR="$USERGOOGDIR\\Default\\Local Storage"

ROOTBAKDIR=$BACKUP/root/chrome
USERBAKDIR=$BACKUP/velda/chrome

mkdir -p $ROOTBAKDIR
mkdir -p $USERBAKDIR

echo ROOTBAKDIR=$ROOTBAKDIR
echo USERBAKDIR=$USERBAKDIR
echo ROOTGOOGDIR=$ROOTGOOGDIR
echo USERGOOGDIR=$USERGOOGDIR
echo ROOTGOOGLOCALSTGDIR=$ROOTGOOGLOCALSTGDIR
echo USERGOOGLOCALSTGDIR=$USERGOOGLOCALSTGDIR
